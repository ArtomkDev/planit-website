export interface LegalSection {
  id: string;
  title: string;
  content: string;
}

export interface LegalDocumentData {
  title: string;
  summary: string;
  effectiveDate: string;
  readTime: number;
  sections: LegalSection[];
}

interface SectionSpec {
  id: string;
  title: string;
  marker?: string;
}

function numberClauses(content: string, sectionNumber: number) {
  let clauseNumber = 0;

  return content.replace(/<p([^>]*)>/g, (_match, attributes: string) => {
    clauseNumber += 1;
    return `<p${attributes}><span data-legal-clause>${sectionNumber}.${clauseNumber}</span>`;
  });
}

function createSections(
  content: string,
  repeatedTitle: string | null,
  specs: SectionSpec[],
): LegalSection[] {
  let source = content.trim();

  if (repeatedTitle) {
    source = source.replace(`<strong>${repeatedTitle}</strong>`, "").trim();
  }

  return specs.map((spec, index) => {
    const marker = spec.marker ? `<strong>${spec.marker}</strong>` : null;
    const nextMarker = specs[index + 1]?.marker
      ? `<strong>${specs[index + 1].marker}</strong>`
      : null;
    const start = marker ? source.indexOf(marker) + marker.length : 0;
    const end = nextMarker ? source.indexOf(nextMarker) : source.length;
    const sectionContent = source
      .slice(start, end)
      .trim()
      .replace(/^<br>\s*/, "")
      .replace(/<br>\s*$/, "");

    return {
      id: spec.id,
      title: spec.title,
      content: numberClauses(sectionContent, index + 1),
    };
  });
}

